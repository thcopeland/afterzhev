; Some items, like weapons and armor, are overlaid on character sprites, and are
; therefore animated. Weapons have 18 sprites: 8 for walking, 2 for idle, 8 for
; the attack. Wearable items have 20 sprites: 16 for walking and 4 for idle. For
; weapon animations, the down-facing and right-facing sprites are mirrored and
; reused as the up-facing and left-facing sprites. This doesn't work for
; wearable items which should look different from different views.
;
; To save memory and cycles, each sprite has specific dimensions and offsets from
; the wielding character. This is stored along with a pointer to the sprite pixels
; in the animated_item_table. This allows a sprite to reuse image data.
;
; Animated sprite table layout:
;   two types of entry: wearable and weapon. The code performing the lookup
;   should know (based on the index) whether what type it is.
;
; Wearable sprite entry (40 bytes)
;   16 (pointers to) walk down, right, up, left sprites (32 bytes)
;   4 idle down, right, up, left sprites (8 bytes)
;
; Weapon sprite entry (40 bytes)
;   8 walk down, right sprites (16 bytes)
;   2 idle down, right sprites (4 bytes)
;   8 attack down, right sprites (16 bytes)
;   2 padding slots (4 bytes)
;
; Non-animated items, like potions and amulets, have only a single fixed-size
; sprite, stored in the static_item_sprite_table. All animated items also have
; an entry in this table, which is used for the inventory view and for loose items.
.equ PADDING = TRANSPARENT

; Produces four bytes with the following layout
; [x] [y] [low ptr] [high ptr]
.macro item_animation_entry ; x, y  pointer
    .db @0, @1
    .dw (_@2&0xffff) << 1
.endm

.macro empty_animation_entry
    .dw 0, 0
.endm

